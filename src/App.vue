<script setup>
import {ref} from 'vue'

let todos = ref(['','',''])
let touch = ref('')

function uno(){
  todos.value.push(touch.value)
  touch.value= ''
}

function deleteI (index) {
  todos.value.splice(index, 1)

}
</script>

<template>
<h1>My Todo Application</h1>
<ol>
<li v-for="(todo, index) in todos">
  <button @click="deleteI">delete</button>
  {{todo}}
  {{ index }}
</li>
</ol>
<input v-model="touch" @keydown.enter="uno">
<button @click="uno">Add Todo</button>
</template>

